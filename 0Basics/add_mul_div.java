public class add_mul_div {
    public static void main(String[] args)
    {
        int a = 10;
        int b = 20;
        int sum = a + b;
        int mul = a * b;
        int div = a / b;
        System.out.println(sum);
        System.out.println(mul);
        System.out.println(div);
    }
}